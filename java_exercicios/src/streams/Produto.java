package streams;

public class Produto {
	final String nome;
	final double preco;
	final double desconto;
	final boolean masculino;
	
	public Produto(String nome, double preco, double desconto, boolean masculino) {
		this.nome = nome;
		this.preco = preco;
		this.desconto = desconto;
		this.masculino = masculino;
	}
	
	

}
